--PROCEDIMIENTO CON BLOQUE PL/SQL
CREATE OR REPLACE PROCEDURE SP_EJEMPLO_PLSQL
AS   
BEGIN
DECLARE 
    V_NUMERO NUMBER;
BEGIN
V_NUMERO:= 14;
IF V_NUMERO > 0 THEN    
DBMS_OUTPUT.PUT_LINE(' POSITIVO');

ELSE  
DBMS_OUTPUT.PUT_LINE('NEGATIVO');
END IF; 

END;
END;

BEGIN
    SP_EJEMPLO_PLSQL;

END;

